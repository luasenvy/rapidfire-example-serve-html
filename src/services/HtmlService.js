class HtmlService extends Interfaces.Service {
  /**
   * [constructor description]
   * @return {[type]} options.router     [description]
   */
  constructor({ router }) {
    super()

    router.get('*', async (req, res, next) => await this.serveHtml(req, res).catch(next))
  }

  async serveHtml(req, res) {
    const filename = /^\/?$/.test(req.url) ? '/index.html' : req.url
    res.sendFile(pathJoin(this.docroot, filename))
  }

  get docroot() {
    return pathJoin(this.$rapidfire.env.paths.root, 'src/public')
  }
}

